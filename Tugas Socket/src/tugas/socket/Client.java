    private void connectActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_connectActionPerformed
        // TODO add your handling code here:
        if (connected == false) 
        {
            username = user.getText();
            user.setEditable(false);

            try 
            {
                client = new Socket("localhost", port);
                InputStreamReader sr = new InputStreamReader(client.getInputStream());
                read = new BufferedReader(sr);
                write = new PrintWriter(client.getOutputStream());
                write.println(username + ":has connected.:Connect");
                write.flush(); 
                connected = true; 
            } 
            catch (Exception ex) 
            {
                chat_area.append("Cannot Connect! Try Again. \n");
                user.setEditable(true);
            }
            
            ListenThread();
            
        } else if (connected == true) 
        {
            chat_area.append("You are already connected. \n");
        }
    }//GEN-LAST:event_connectActionPerformed

