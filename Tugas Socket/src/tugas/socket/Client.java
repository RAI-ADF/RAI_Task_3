    public class Reader implements Runnable {

        @Override
        public void run() {
            String[] data;
            String stream, done = "Done", connect = "Connect", disconnect = "Disconnect", chat = "Chat";

            try {
                while ((stream = read.readLine()) != null) {
                    data = stream.split(":");
                    if (data[2].equals(chat)) {
                        chat_area.append(data[0] + ": " + data[1] + "\n");
                        chat_area.setCaretPosition(chat_area.getDocument().getLength());
                    } else if (data[2].equals(connect)) {
                        chat_area.removeAll();
                        userAdd(data[0]);
                    } else if (data[2].equals(disconnect)) {
                        userRemove(data[0]);
                    } else if (data[2].equals(done)) {
                        writeUsers();
                        users.clear();
                    }
                }
            } catch (Exception ex) {
                System.out.println(ex);
            }
        }
    }

    public void ListenThread() {
        Thread Reader = new Thread(new Reader());
        Reader.start();
    }

    public void userAdd(String user) {
        users.add(user);
    }

    public void userRemove(String user) {
        chat_area.append(user + " is now offline.\n");
    }

    public void writeUsers() {
        String[] tempList = new String[(users.size())];
        users.toArray(tempList);
    }

    public void ClickDisconnect() {
        String done = (username + ": :Disconnect");
        try {
            write.println(done);
            write.flush();
        } catch (Exception e) {
            chat_area.append("Couldn't send Disconnect\n");
        }
    }

    public void Disconnect() {
        try {
            chat_area.append("Disconnected.\n");
            client.close();
        } catch (Exception ex) {
            chat_area.append("Failed to disconnect. \n");
        }
        connected = false;
        user.setEditable(true);

    }


