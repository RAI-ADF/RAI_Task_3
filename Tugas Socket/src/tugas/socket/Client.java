    private void sendActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_sendActionPerformed
        // TODO add your handling code here:
        String nothing = "";
        if ((text.getText()).equals(nothing)) {
            text.setText("");
            text.requestFocus();
        } else {
            try {
               write.println(username + ":" + text.getText() + ":" + "Chat");
               write.flush(); // flushes the buffer
            } catch (Exception ex) {
                chat_area.append("Message was not sent. \n");
            }
            text.setText("");
            text.requestFocus();
        }

        text.setText("");
        text.requestFocus();
    }//GEN-LAST:event_sendActionPerformed

